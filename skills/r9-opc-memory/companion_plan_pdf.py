    pdf.paragraph('OPC投顾服务部基于6月20日投决会决议，制定本陪伴服务方案，目标是在「产业趋势未结束」与「客户体验优先」之间取得平衡，帮助客户理性看待AI投资机会，避免追涨杀跌。')

    pdf.h2('二、投决会核心结论回顾')
    pdf.bullet('AI产业链整体尚未涨到头，但普涨阶段已结束，当前进入「结构α」分化阶段。')
