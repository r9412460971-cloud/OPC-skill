    pdf.bullet('本方案所有涉及具体标的的内容，均需附带风险提示，不得暗示「必涨」「稳赚」。')
    pdf.bullet('AI芯片、人形机器人、AI应用等高波动赛道，必须与客户风险测评等级匹配，不得超适当性推荐。')
