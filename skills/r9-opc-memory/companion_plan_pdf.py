    pdf.bullet('所有对外材料须经Sage合规审核后方可发布。')

    pdf.h2('九、服务目标与考核指标')
    pdf.bullet('L2/L3客户AI持仓再平衡沟通覆盖率：≥90%（截至6月27日）。')
    pdf.bullet('客户对AI主题的风险认知度提升：通过回访抽样评估。')
